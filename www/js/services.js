var wink = angular.module('wink.services', [])

//Post Service
wink.factory('postService', function($firebaseArray) {
	var fb = new Firebase("https://wink-connections.firebaseio.com/posts");
	var posts = $firebaseArray(fb);
	var postService= {
		all: posts,
		get: function(postID) {
			return posts.$getRecord(postID);
		}
	};
	return postService;
});

